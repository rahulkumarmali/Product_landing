        A natural product is a natural compound or substance produced by 
        a living organismâ€”that is, found in nature. In the broadest sense,
        natural products include any substance produced by life.Natural 
        products can also be prepared by chemical synthesis (both semisynthesis
        and total synthesis) and have played a central role in the development
        of the field of organic chemistry by providing challenging synthetic 
        targets. The term natural product has also been extended for commercial 
        purposes to refer to cosmetics, dietary supplements, and foods produced
        from natural sources without added artificial ingredients.
        Within the field of organic chemistry, 
        the definition of natural products is usually restricted to organic compounds 
        isolated from natural sources that are produced by the pathways of primary
        or secondary metabolism.Within the field of medicinal chemistry,
        the definition is often further restricted to secondary metabolites.
        Secondary metabolites (or specialized metabolites) are not essential for survival,
        but nevertheless provide organisms that produce them an evolutionary advantage.
        Many secondary metabolites are cytotoxic and have been selected and 
        optimized through evolution for use as "chemical warfare" agents against prey,
       predators, and competing organisms.Secondary or specialized metabolites
       are often unique to species, which is contrasted to primary metabolites which 
       have broad use across kingdoms. Secondary metabolites are marked by chemical
       complexity which is why they are of such interest to chemists.
       Natural sources may lead to basic research on potential bioactive
       components for commercial development as lead compounds in drug discovery.
      Although natural products have inspired numerous drugs, 
       drug development from natural sources has received declining attention in the
       21st century by pharmaceutical companies, partly due to unreliable access and supply, 
       intellectual property, cost, and profit concerns, seasonal or environmental variability 
       of composition, and loss of sources due to rising extinction rates.
